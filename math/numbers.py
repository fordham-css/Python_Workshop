# Shows how numbers work in python

# An integer
a = 4
print(a)
# A floating point number
b = 4.3
print(b)
# Round down a float
print(int(4.3))

# A string
c = "1000"

# If you try to do math on it, weird stuff will
# happen

print(c * 5)

# So, convert a string to a number
print(int(c) * 5)